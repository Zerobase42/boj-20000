void solve3(char b[1024][2048],int sx,int sy,char c,int len){
    int rs,re;
    if(c=='/'){rs=sx-1024+sy+1;re=sx+1;}
    else{rs=sx;re=rs+1024-sy;}
    bool stop=1;
    for(int i=rs;i<re;i++)if(last_row[i]!=' ')stop=0;
    if(stop)return;
    bool rec=1;
    if(!len){len=1;rec=0;}
    int dx=c=='/'?-1:1,dy=1;
    for(int i=0;i<len;i++){int x=sx+dx*i,y=sy+dy*i;if(0<=x&&x<2048&&0<=y&&y<1024)b[y][x]=c;}
    if(!rec)return;
    solve3(b,sx+dx*len,sy+dy*len,c,len/2);
    solve3(b,sx+dx*(len-1),sy+dy*(len-1)+1,c=='/'?'\\':'/',len/2);
}

